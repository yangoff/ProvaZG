package model;

public class Sku_produto {
	
	private String id;
	private float preco_u;
	
	
	
	
	public Sku_produto(String id, float preco_u) {
		super();
		this.id = id;
		this.preco_u = preco_u;
		
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public float getPreco_u() {
		return preco_u;
	}
	public void setPreco_u(float preco_u) {
		this.preco_u = preco_u;
	}
	
	
	
	
	
	
	
		
	
	

}
